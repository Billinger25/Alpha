function submitSlotTerm() {
  const typed = elements.slotAnswerInput.value.trim();
  if (!typed) return;

  const normalizedTyped = normalizeTerm(typed);
  const match = state.slotTerms.find((term) => normalizeTerm(term) === normalizedTyped);

  if (!match) {
    setFeedback("That term is not in this slot set.", "incorrect");
    elements.slotAnswerInput.select();
    return;
  }

  if (state.filledSlotTerms.includes(match)) {
    setFeedback("That term is already filled.", "incorrect");
    elements.slotAnswerInput.select();
    return;
  }

  state.filledSlotTerms.push(match);
  state.review = state.slotTerms
    .filter((term) => !state.filledSlotTerms.includes(term))
    .map((term) => ({ term }));
  renderSlots();

  if (state.filledSlotTerms.length === state.slotTerms.length) {
    elements.feedbackTag.textContent = "Complete";
    elements.checkButton.disabled = true;
    elements.slotAnswerInput.disabled = true;
    setFeedback("All 10 slots are filled.", "correct");
  } else {
    elements.feedbackTag.textContent = "Typing";
    setFeedback(`${state.filledSlotTerms.length} of ${state.slotTerms.length} slots filled.`, "correct");
  }

  updateStats();
  renderReview();
}

