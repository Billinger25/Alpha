function moveMatchCard(card, targetZone) {
  if (!card || !targetZone) return;

  const originZone = card.parentElement;
  const existingCard =
    targetZone.dataset.zone === "dropzone" ? targetZone.querySelector(".match-card") : null;

  if (existingCard && existingCard !== card) {
    originZone.append(existingCard);
  }

  if (targetZone.dataset.zone === "dropzone") {
    targetZone.textContent = "";
  }

  targetZone.append(card);
  resetEmptyMatchDropzones();
  clearMatchMarks();
  state.selectedMatchCard = null;
  elements.matchBoard.querySelectorAll(".match-card").forEach((matchCard) => {
    matchCard.classList.remove("selected");
  });
}

