function checkAnswer() {
  if (state.mode === "order") {
    checkOrder();
    return;
  }

  if (state.mode === "match") {
    checkMatches();
    return;
  }

  if (state.mode === "slots") {
    submitSlotTerm();
    return;
  }

  if (state.checked || !state.selectedChoice) return;

  const current = state.questions[state.currentIndex];
  const isCorrect = state.selectedChoice === current.answer;

  state.checked = true;
  elements.checkButton.disabled = true;
  elements.nextButton.disabled = false;

  elements.choiceList.querySelectorAll(".choice-button").forEach((button) => {
    button.disabled = true;
    button.classList.toggle("correct-choice", button.textContent === current.answer);
    button.classList.toggle("incorrect-choice", button.textContent === state.selectedChoice && !isCorrect);
  });

  if (isCorrect) {
    state.score += 1;
    elements.feedbackTag.textContent = "Correct";
    setFeedback("Correct.", "correct");
  } else {
    elements.feedbackTag.textContent = "Review";
    state.review.push({
      question: current.question,
      given: state.selectedChoice,
      correct: current.answer,
    });
    setFeedback(`Not quite. Correct answer: ${current.answer}`, "incorrect");
  }

  updateStats();
  renderReview();
}

