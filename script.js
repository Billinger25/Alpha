function enableMatchDragging() {
  const zones = elements.matchBoard.querySelectorAll(".match-dropzone, .match-bank");

  elements.matchBoard.querySelectorAll(".match-card").forEach((card) => {
    card.addEventListener("dragstart", (event) => {
      event.dataTransfer.setData("text/plain", card.dataset.value);
      card.classList.add("dragging");
    });

    card.addEventListener("dragend", () => {
      card.classList.remove("dragging");
    });
  });

  zones.forEach((zone) => {
    zone.addEventListener("dragover", (event) => {
      event.preventDefault();
      zone.classList.add("drag-over");
    });

    zone.addEventListener("dragleave", () => {
      zone.classList.remove("drag-over");
    });

    zone.addEventListener("drop", (event) => {
      event.preventDefault();
      zone.classList.remove("drag-over");
      const value = event.dataTransfer.getData("text/plain");
      const card = elements.matchBoard.querySelector(`.match-card[data-value="${CSS.escape(value)}"]`);
      moveMatchCard(card, zone);
    });

    zone.addEventListener("click", () => {
      if (state.selectedMatchCard) {
        moveMatchCard(state.selectedMatchCard, zone);
      }
    });
  });
}

