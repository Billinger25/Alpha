function enableSortDragging() {
  let draggedItem = null;

  elements.sortList.addEventListener("dragstart", (event) => {
    draggedItem = event.target.closest(".sort-item");
    if (!draggedItem) return;
    draggedItem.classList.add("dragging");
    event.dataTransfer.effectAllowed = "move";
  });

  elements.sortList.addEventListener("dragend", () => {
    draggedItem?.classList.remove("dragging");
    draggedItem = null;
  });

  elements.sortList.addEventListener("dragover", (event) => {
    event.preventDefault();
    const afterElement = getDragAfterElement(event.clientY);
    if (!draggedItem) return;
    clearSortMarks();
    if (afterElement == null) {
      elements.sortList.append(draggedItem);
    } else {
      elements.sortList.insertBefore(draggedItem, afterElement);
    }
  });
}

function getDragAfterElement(y) {
  const draggableElements = [...elements.sortList.querySelectorAll(".sort-item:not(.dragging)")];

  return draggableElements.reduce(
    (closest, child) => {
      const box = child.getBoundingClientRect();
      const offset = y - box.top - box.height / 2;
      if (offset < 0 && offset > closest.offset) {
        return { offset, element: child };
      }
      return closest;
    },
    { offset: Number.NEGATIVE_INFINITY, element: null },
  ).element;
}

