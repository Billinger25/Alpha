function parseQuiz(text) {
  return text
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const parts = line
        .split("|")
        .map((item) => item.trim())
        .filter(Boolean);
      if (parts.length < 2) return null;

      return {
        question: parts[0],
        answer: parts[1],
        choices: parts.slice(1),
      };
    })
    .filter(Boolean);
}

function parseOrder(text) {
  return parseOrderSorts(text).flatMap((sort) => sort.items);
}

function parseMatch(text) {
  return text
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const parts = line
        .split("|")
        .map((item) => item.trim())
        .filter(Boolean);
      if (parts.length < 2) return null;
      return { term: parts[0], match: parts[1] };
    })
    .filter(Boolean);
}

function parseSlots(text) {
  return text
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .slice(0, 10);
}

function parsePoems(text) {
  return text
    .split(/\n\s*\n/g)
    .map((block) => block.trim())
    .filter(Boolean)
    .map((block, index) => {
      const lines = block.split("\n");
      const firstLine = lines.shift()?.trim() || `Poem ${index + 1}`;
      const title = firstLine.endsWith(":") ? firstLine.replace(/:$/, "").trim() : firstLine;
      const poem = lines.join("\n").trim();
      return title && poem ? { title, poem } : null;
    })
    .filter(Boolean);
}

function parseParagraphs(text) {
  return text
    .split(/\n\s*\n/g)
    .map((paragraph) => paragraph.trim())
    .filter(Boolean);
}

function parseOrderSorts(text) {
  return text
    .split(/\n\s*\n/g)
    .map((block) =>
      block
        .split("\n")
        .map((line) => line.trim())
        .filter(Boolean),
    )
    .filter((lines) => lines.length)
    .map((lines, index) => {
      const hasTitle = lines.length > 2 && lines[0].endsWith(":");
      const title = hasTitle ? lines[0].replace(/:$/, "").trim() : `Sort ${index + 1}`;
      const items = hasTitle ? lines.slice(1) : lines;
      return { title, items };
    })
    .filter((sort) => sort.items.length);
}

function parseLegacyOrder(text) {
  return text
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);
}

function shuffle(items) {
  return items
    .map((item) => ({ item, order: Math.random() }))
    .sort((a, b) => a.order - b.order)
    .map(({ item }) => item);
}

function unique(items) {
  return [...new Set(items.filter(Boolean))];
}

